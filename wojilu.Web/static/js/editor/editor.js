wojilu.editor.prototype.render = function() {
    var _x = this;
    if( wojilu.editorConfig.isSupport() == false ) return;
    _x.beginRender().addImgs().makeWritable().addCallback().resize();
    $('#'+_x.frmId).before($('#'+_x.name));
};

