wojilu.editor.prototype.makeWritable = function () {
    
    var _x = this;
    var htmlContent = _x.html;
    if( !document.all && wojilu.str.isNull( _x.html ) ) {
        htmlContent = '<p>&nbsp;</p>'+_x.html;
    }
    var frameHtml = _x.getFrmBody( htmlContent );

    if( document.all ) { _x.editor = frames[ _x.frmId ]; } else { _x.editor = _x.$id( _x.frmId ).contentWindow; };
    _x.doc = _x.editor.document;

    if (document.all) {
        _x.writeContentToEditor(frameHtml);            
        if (!_x.IsReadOnly) _x.doc.designMode = 'on';            
    }
    else {
        if (!_x.IsReadOnly) _x.$id(_x.frmId).contentDocument.designMode = 'on';
        _x.writeContentToEditor(frameHtml);
    };

    _x.addBlur();

    $(_x.doc).bind('paste',function(e){
        setTimeout( function() {
            _x.clearFormat();
        }, 30 );
    });

    return _x;
};

wojilu.editor.prototype.resize = function() {
    
    var _x = this;
    var _clientY;
    var frmDoc = _x.doc;
    var doc = document;
    var $resizer =$('.resizer', _x.context ); 

    $resizer.mousedown( function(e) {

        e.stopPropagation();

        function bindMove(e) {
            e.preventDefault();

            var clientY = wojilu.position.getMouse(e).y;
            var deltaY = clientY - _clientY; 
            _clientY = clientY;

            var $frm = $('.wojiluEditorFrame', _x.context);
            var newHeight = $frm.height() + deltaY;
            $frm.height( newHeight );
            $frm.prev().height( newHeight );
        }
    
        function bindUp(e) {
            e.preventDefault();
            $(doc).unbind('mousemove',  bindMove ).unbind('mouseup',  bindUp ).unbind( 'selectstart', bindSelect );
            $(frmDoc).unbind('mouseup', bindUp).unbind('selectstart',bindSelect);
            if( $resizer.releaseCapture ) $resizer.releaseCapture();
        }

        function bindSelect(e) { e.preventDefault(); }

        $(doc).mousemove( bindMove ).mouseup( bindUp ).bind( 'selectstart', bindSelect );
        $(frmDoc).mouseup( bindUp ).bind( 'selectstart', bindSelect );

        if( $resizer.setCapture ) $resizer.setCapture();
    });
    return _x;
};


